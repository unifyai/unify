"""Global builtin-guidance catalogue: stable ids and idempotent seeding.

The catalogue lives in the public-read builtins project (see
``unify.common.builtins``) owned by the platform admin account. It stores
exactly one platform-wide copy of every builtin guidance entry; every
tenant reads it by default and never writes to it.

Two sources feed the default seeding set:

* the committed snapshot ``builtins_guidance.json`` next to this module,
  generated by ``scripts.skill_migration.builtins_import`` from upstream
  Agent Skills repositories pinned in a lockfile manifest, and
* ``PLATFORM_GUIDANCE_ENTRIES`` below — platform-authored entries versioned
  directly in code (kept out of the snapshot so importer regeneration can
  never drop them).

Seeding is hash-guarded per entry: only changed or removed entries are
deleted and re-inserted, so repeated runs are cheap and idempotent.

Builtin rows carry stable hash-based ``guidance_id`` values (derived from
the namespaced title) so they never collide semantically with tenant
auto-counting ids, and ``is_builtin=True`` for unambiguous provenance.
"""

from __future__ import annotations

import hashlib
import json
import logging
from pathlib import Path
from typing import Dict, List, Optional

import unisdk
from unisdk.utils.http import RequestError

from ..common.builtins import (
    builtins_project,
    ensure_builtins_project,
    read_seed_hashes,
    write_seed_hashes,
)
from ..common.embed_utils import ensure_vector_column
from ..common.model_to_fields import model_to_fields
from .types.guidance import Guidance

logger = logging.getLogger(__name__)

BUILTINS_GUIDANCE_CONTEXT = "Guidance"
BUILTINS_GUIDANCE_META_CONTEXT = "Guidance/Meta"
_HASH_MAP_KEY = "guidance_hash_by_skill"

# The backend refuses to embed inputs above ~8000 estimated tokens (0.25 per
# UTF-8 byte, i.e. ~32KB). Imported skills with inlined scripts routinely
# exceed that, so the content embedding ranks on a truncated head of the
# entry — where the description and body intro carry the retrieval signal —
# while the stored ``content`` column keeps the full text.
CONTENT_EMBED_HEAD_CHARS = 24000

SNAPSHOT_PATH = Path(__file__).with_name("builtins_guidance.json")

# Platform-authored builtin guidance, seeded alongside the imported-skills
# snapshot. Versioned in code (not in the snapshot) so importer regeneration
# can never drop it; edits here reseed automatically via the same per-entry
# hash guard.
